                <li>
                    <p class="text-justify">
                        <b>UCS (Universal Coverage Scheme):</b> Thailand’s largest public health insurance program, introduced in 2002 and is administered by the National Health Security Office. It covers Thai citizens who are not eligible for either SSS or CSMBS—primarily informal sector workers, farmers, the unemployed, and other non-salaried groups.
                        The scheme is fully financed through general taxation, meaning beneficiaries do not pay monthly contributions. UCS emphasizes cost control and equity by using capitation payments for outpatient care and Diagnosis-Related Groups (DRG) under a global budget for inpatient services.
                        Most participating providers are public hospitals, and beneficiaries must typically register with a primary care network, which acts as a gatekeeper for referrals. This structure promotes strong budget discipline and nationwide access, but limits provider choice compared to other schemes.
                    </p>
                </li>

                <!-- SSS -->
                <li>
                    <p class="text-justify">
                        <b> SSS (Social Security Scheme):</b> Managed by the Social Security Office under the Ministry of Labor. It covers private-sector employees in the formal economy, including registered migrant workers.
                        Unlike UCS, SSS operates on a contributory model funded through tripartite payroll contributions from employees, employers, and the government. Members pay a salary-based monthly contribution (subject to a ceiling).
                        Healthcare providers (public and private) contract with the Social Security Office and receive annual capitation payments per registered worker, with additional DRG reimbursement for high-cost inpatient cases.
                        Compared to UCS, SSS offers broader private hospital participation and integrates healthcare with other social benefits such as pensions, unemployment compensation, and disability benefits. However, its benefit structure is generally less generous than CSMBS.
                    </p>
                </li>

                <!-- CSMBS -->
                <li>
                    <p class="text-justify">
                        <b>CSMBS (Civil Servant Medical Benefit Scheme):</b> Designed for government civil servants, retirees, and their dependents. It is administered by the Comptroller General’s Department under the Ministry of Finance and is financed entirely through general taxation.
                        Beneficiaries are not required to make specific healthcare contributions. Historically, CSMBS reimbursed providers on a fee-for-service basis for outpatient care, while inpatient services are reimbursed under DRG. This payment structure has made CSMBS the most generous scheme in terms of benefit coverage and per-capita expenditure.
                        Civil servants typically have greater flexibility, particularly at tertiary and teaching hospitals, and utilization rates tend to be higher than under UCS or SSS.
                    </p>
                </li>

